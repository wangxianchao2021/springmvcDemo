package com.bewg.token.entity;

public class DDParam {

	/** 英文名 */
	private String enName = "";
	/** 中文名 */
	private String cname = "";
	/** 值 */
	private String evalue = "";
	/** 备注 */
	private String memo = "";
	/** 备注1 */
	private String memo1 = "";

	/**
	 *<pre> 执行获取英文名处理 </pre>
	 * @return enName 英文名
	 */
	public String getEnName() {
		return enName;
	}

	/**
	 *<pre> 执行设定英文名处理 </pre>
	 * @param enName 英文名
	 */
	public void setEnName(String enName) {
		this.enName = enName;
	}

	/**
	 *<pre> 执行获取中文名处理 </pre>
	 * @return cname 中文名
	 */
	public String getCname() {
		return cname;
	}

	/**
	 *<pre> 执行设定中文名处理 </pre>
	 * @param cname 中文名
	 */
	public void setCname(String cname) {
		this.cname = cname;
	}

	/**
	 *<pre> 执行获取值处理 </pre>
	 * @return evalue 值
	 */
	public String getEvalue() {
		return evalue;
	}

	/**
	 *<pre> 执行设定值处理 </pre>
	 * @param evalue 值
	 */
	public void setEvalue(String evalue) {
		this.evalue = evalue;
	}

	/**
	 *<pre> 执行获取备注处理 </pre>
	 * @return memo 备注
	 */
	public String getMemo() {
		return memo;
	}

	/**
	 *<pre> 执行设定备注处理 </pre>
	 * @param memo 备注
	 */
	public void setMemo(String memo) {
		this.memo = memo;
	}

	/**
	 *<pre> 执行获取备注1处理 </pre>
	 * @return memo1 备注1
	 */
	public String getMemo1() {
		return memo1;
	}

	/**
	 *<pre> 执行设定备注1处理 </pre>
	 * @param memo1 备注1
	 */
	public void setMemo1(String memo1) {
		this.memo1 = memo1;
	}
}
